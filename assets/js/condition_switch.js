let buah = prompt("Pilih buah : pisang, jeruk atau apel");
switch (buah) {
  case "pisang":
    alert("Saya suka buah pisang");
    break;
  case "jeruk":
    alert("Saya tidak terlalu suka buah jeruk");
    break;
  case "apel":
    alert("Saya hanya menyukai apel yang berwarna merah");
    break;
  default:
    alert("Kamu tidak memilih buah sesuai pilihan");
    break;
}
